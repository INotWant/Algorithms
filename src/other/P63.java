    public int uniquePathsWithObstacles(int[][] obstacleGrid) {
        int m = obstacleGrid.length;
        int n = obstacleGrid[0].length;
        int[][] array = new int[m][n];
        for (int i = 0; i < m; i++)
            Arrays.fill(array[i], -1);
        return helper(m - 1, n - 1, array, obstacleGrid);
    }

    private static int helper(int i, int j, int[][] array, int[][] obstacleGrid) {
        if (array[i][j] != -1)
            return array[i][j];
        if (obstacleGrid[i][j] == 1)
            return 0;
        if (i == 0 && j == 0)
            return 1;
        else {
            if (i == 0) {
                array[i][j] = helper(i, j - 1, array, obstacleGrid);
                return array[i][j];
            } else if (j == 0) {
                array[i][j] = helper(i - 1, j, array, obstacleGrid);
                return array[i][j];
            } else {
                array[i][j] = helper(i - 1, j, array, obstacleGrid) + helper(i, j - 1, array, obstacleGrid);
                return array[i][j];
            }
        }
    }


